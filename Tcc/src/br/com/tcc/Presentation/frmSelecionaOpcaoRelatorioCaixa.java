    private void btnTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTodosActionPerformed
        try {
            //Arquivo do Relatorio
            //String relatorio = "/META-INF/relatorio/relatorioEstoque.jasper";
            InputStream relatorio = this.getClass().getClassLoader().getResourceAsStream("META-INF/relatorio/relatorioCaixa.jasper");
            //Lista a ser exibida no relatorio
            CaixaDAO caixaDao = new CaixaDAO();
            List<Caixa> caixas = new LinkedList<>();
            
            for(Caixa c :caixaDao.ListarTodos()){
                c.setValorTotal(c.calcularTotalCaixa());
                caixas.add(c);
            }
            

            //Fonte de dados
            JRBeanCollectionDataSource fonteDados = new JRBeanCollectionDataSource(caixas);

            //Gera o Relatorio
            JasperPrint relatorioGerado = JasperFillManager.fillReport(relatorio, null, fonteDados);

            //Exibe o Relatorio
            JasperViewer jasperViewer = new JasperViewer(relatorioGerado, false);
            this.dispose();
            jasperViewer.setVisible(true);
            
        }catch(JRException e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnTodosActionPerformed

    private void btnFornecedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFornecedorActionPerformed

        try {
//Pegando o id do Produto

            String funcionario = JOptionPane.showInputDialog("Insira o Id do Funcionário");

            //Arquivo do Relatorio
            //String relatorio = "/META-INF/relatorio/relatorioEstoque.jasper";
            InputStream relatorio = this.getClass().getClassLoader().getResourceAsStream("META-INF/relatorio/relatorioCaixa.jasper");
            //Lista a ser exibida no relatorio

            CaixaDAO caixasDao = new CaixaDAO();

            List<Caixa> caixas = caixasDao.ListarTodos();
            List<Caixa> caixasFiltro = new LinkedList<>();
            for (Caixa c : caixas) {
                if (c.getFuncionario().getId().intValue() == Integer.parseInt(funcionario)) {
                    c.setValorTotal(c.calcularTotalCaixa());
                    caixasFiltro.add(c);
                }
            }

            if (!caixasFiltro.isEmpty()) {

                //Fonte de dados
                JRBeanCollectionDataSource fonteDados = new JRBeanCollectionDataSource(caixasFiltro);

                //Gera o Relatorio
                JasperPrint relatorioGerado = JasperFillManager.fillReport(relatorio, null, fonteDados);

                //Exibe o Relatorio
                JasperViewer jasperViewer = new JasperViewer(relatorioGerado, false);
                this.dispose();
                jasperViewer.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Funcionário não encontrado !");
            }

        } catch (JRException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Id inválido !");
        }

    }//GEN-LAST:event_btnFornecedorActionPerformed

